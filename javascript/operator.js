// Arithmatic operator

let a = 10;
let b = 5;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);


// unary operator

console.log(a++); //10
console.log(++a);  // 12


// Assignment operator

let c = 10;
let d = 5;
d  = c;
console.log(d);


